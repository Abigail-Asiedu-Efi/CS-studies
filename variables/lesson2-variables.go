package main

import "fmt"

const salutation = "Hello, Learners!"

func main() {

	fmt.Println(salutation)

	var a string = "Programming"
	fmt.Println(a)

	var b, c, d int = 2, 7, 6
	fmt.Println(b, c, d)

	e := false
	fmt.Println(e)

	var f int
	fmt.Println(f)

	var m int = 24
	var n float64 = 2.56

	fmt.Println("Integer: ", m)
	fmt.Println("Float: ", n)

	var userName = "Abigail"
	fmt.Println(userName)

}
